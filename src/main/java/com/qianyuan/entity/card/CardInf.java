package com.qianyuan.entity.card;


public class CardInf {

  private long xh;
  private String kh;
  private double cash;
  private long zt;
  private String yhm;
  private String bz;


  public long getXh() {
    return xh;
  }

  public void setXh(long xh) {
    this.xh = xh;
  }


  public String getKh() {
    return kh;
  }

  public void setKh(String kh) {
    this.kh = kh;
  }


  public double getCash() {
    return cash;
  }

  public void setCash(double cash) {
    this.cash = cash;
  }


  public long getZt() {
    return zt;
  }

  public void setZt(long zt) {
    this.zt = zt;
  }


  public String getYhm() {
    return yhm;
  }

  public void setYhm(String yhm) {
    this.yhm = yhm;
  }


  public String getBz() {
    return bz;
  }

  public void setBz(String bz) {
    this.bz = bz;
  }

}
