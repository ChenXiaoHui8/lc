package com.qianyuan.entity.card;


public class CardYw {

  private long xh;
  private String kh;
  private double cash;
  private String type;
  private java.sql.Timestamp sj;
  private String sbbh;


  public long getXh() {
    return xh;
  }

  public void setXh(long xh) {
    this.xh = xh;
  }


  public String getKh() {
    return kh;
  }

  public void setKh(String kh) {
    this.kh = kh;
  }


  public double getCash() {
    return cash;
  }

  public void setCash(double cash) {
    this.cash = cash;
  }


  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }


  public java.sql.Timestamp getSj() {
    return sj;
  }

  public void setSj(java.sql.Timestamp sj) {
    this.sj = sj;
  }


  public String getSbbh() {
    return sbbh;
  }

  public void setSbbh(String sbbh) {
    this.sbbh = sbbh;
  }

}
