package com.qianyuan.entity.lcxtpt;


public class Camera {

  private long xh;
  private String sbbh;
  private String sbmc;
  private String ip;
  private String port;
  private String yhm;
  private String mm;
  private long sfsc;
  private long flag;


  public long getXh() {
    return xh;
  }

  public void setXh(long xh) {
    this.xh = xh;
  }


  public String getSbbh() {
    return sbbh;
  }

  public void setSbbh(String sbbh) {
    this.sbbh = sbbh;
  }


  public String getSbmc() {
    return sbmc;
  }

  public void setSbmc(String sbmc) {
    this.sbmc = sbmc;
  }


  public String getIp() {
    return ip;
  }

  public void setIp(String ip) {
    this.ip = ip;
  }


  public String getPort() {
    return port;
  }

  public void setPort(String port) {
    this.port = port;
  }


  public String getYhm() {
    return yhm;
  }

  public void setYhm(String yhm) {
    this.yhm = yhm;
  }


  public String getMm() {
    return mm;
  }

  public void setMm(String mm) {
    this.mm = mm;
  }


  public long getSfsc() {
    return sfsc;
  }

  public void setSfsc(long sfsc) {
    this.sfsc = sfsc;
  }


  public long getFlag() {
    return flag;
  }

  public void setFlag(long flag) {
    this.flag = flag;
  }

}
