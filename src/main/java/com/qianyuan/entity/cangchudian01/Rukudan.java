package com.qianyuan.entity.cangchudian01;


public class Rukudan {

  private long id;
  private String liushuihao;
  private long huowuId;
  private long cangkuId;
  private long shuliang;
  private long rukuren;
  private java.sql.Timestamp shijian;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getLiushuihao() {
    return liushuihao;
  }

  public void setLiushuihao(String liushuihao) {
    this.liushuihao = liushuihao;
  }


  public long getHuowuId() {
    return huowuId;
  }

  public void setHuowuId(long huowuId) {
    this.huowuId = huowuId;
  }


  public long getCangkuId() {
    return cangkuId;
  }

  public void setCangkuId(long cangkuId) {
    this.cangkuId = cangkuId;
  }


  public long getShuliang() {
    return shuliang;
  }

  public void setShuliang(long shuliang) {
    this.shuliang = shuliang;
  }


  public long getRukuren() {
    return rukuren;
  }

  public void setRukuren(long rukuren) {
    this.rukuren = rukuren;
  }


  public java.sql.Timestamp getShijian() {
    return shijian;
  }

  public void setShijian(java.sql.Timestamp shijian) {
    this.shijian = shijian;
  }

}
